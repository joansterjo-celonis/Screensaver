export function normalizeFlipDotText(value: string) {
  return value
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .toUpperCase()
    .replace(/[^A-Z0-9 °:.?\-]/g, " ");
}

export function flipDotGlyph(character: string) {
  return FLIP_DOT_GLYPHS[character] ?? FLIP_DOT_GLYPHS["?"];
}

export function weatherDotPattern(icon: WeatherIconName) {
  return WEATHER_PATTERNS[icon] ?? WEATHER_PATTERNS.unknown;
}
